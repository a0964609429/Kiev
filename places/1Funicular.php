<h2> Фуникулёр </h2>

<img src="img/Funicular.jpg" >

<p>
Ки́евский фуникулёр — линия фуникулёра в Киеве (Украина), которая проходит по крутому склону Владимирской горки
и соединяет две части города: Подол с Верхним городом. Это один из первых фуникулёров в бывшей Российской империи,
он был открыт в 1905 году после Кремлёвского и Похвалинского элеваторов в Нижнем Новгороде (1896) и
Одесского фуникулёра (1902). 
</p>

