<h2> Национальная опера Украины </h2>

<img src="img/teatr_shevchenka.jpg" >

<p>
Национальная опера Украины  — крупнейший музыкальный театр Украины, расположенный в Киеве. Здание оперного театра, 
возведённое в 1901 году, по проекту архитектора Виктора Шрётера, находится в центре города, по почтовому адресу: 
Владимирская улица, дом № 50. 
