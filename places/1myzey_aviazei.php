<h2> Музей авиации в Киеве </h2>

<img src="img/myzey_aviazei3.jpg" >

<p>
Музей авиации в Киеве – самый молодой и самый большой историко-технический музей Украины. Его открытие состоялось
в 2003 году и было приурочено к столетию мировой авиации. Музей находится на территории учебной базы НАУ, недалеко 
от аэропорта Жуляны.  
