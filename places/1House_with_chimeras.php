<h2> Дом с химерами </h2>

<img src="img/House_with_chimeras.jpg" >

<p>
Дом с химерами — кирпичное здание в стиле модерн, расположенное в столице Украины, Киеве. 
Своё название дом получил благодаря скульптурным украшениям (расположенным как на фасаде дома так и внутри его),
тематика которых — наземный и подводный животный мир, атрибуты охоты, сказочные существа. 
