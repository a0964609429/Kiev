<h2> Золотые ворота </h2>

<img src="img/Golden_Gate.jpg" >

<p>
Золотые ворота — памятник оборонного зодчества Киевской Руси времён княжения Ярослава Мудрого. 
Исторически служили главным въездом в киевский Верхний город с южной стороны. В наше время реконструкция Золотых ворот, 
включающая некоторые сохранившиеся средневековые элементы, является одной из достопримечательностей Киева.
</p>

