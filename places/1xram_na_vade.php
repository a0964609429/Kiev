<h2> Храм Святителя Чудотворца Николая на водах </h2>

<img src="img/xram_na_vade2.jpg" >

<p>
Храм Святителя Чудотворца Николая на водах — православный храм на Днепре у набережной Подола в Киеве. 
Первый и единственный храм на Украине, расположенный в акватории. Высота 23 метра. С берега к церкви подходит 
15-метровый мостик. Вмещает около 50 человек, ещё столько же — его площадка.
