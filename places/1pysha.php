<h2> Пуща-Водица </h2>

<img src="img/pysha3.jpg" >

<p>
Пуща-Водица — историческая местность в Киеве. Расположена на северо-западной окраине города, по решению Киевского 
городского совета от 8 ноября 2001 года перестала быть посёлком городского типа и должна была быть переименована в 
"Троещина 2", позднее стала частью Оболонского района города.  
</p>

