<h2> Софийская площадь </h2>

<img src="img/safiiska_ploshad2.jpg" >

<p>
Софийская площадь — одна из центральных и древнейших площадей Киева. На Софийской площади расположена колокольня
Софийского собора и памятник Богдану Хмельницкому. Площадь расположена между Владимирской улицей, Владимирским 
проездом, улицей Аллы Тарасовой, Софиевской улицей и Рыльским переулком.
</p>

