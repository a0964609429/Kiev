<h2> Родина-мать </h2>

<img src="img/rodina_mati.jpg" >

<p>
«Родина-мать» — монументальная скульптура в Киеве на правом берегу Днепра. Расположена на территории Музея истории Украины во 
Второй мировой войне. Открыта в составе музейного комплекса в 1981 году в День Победы. Комплекс открывал Генеральный секретарь ЦК 
КПСС Леонид Брежнев.
