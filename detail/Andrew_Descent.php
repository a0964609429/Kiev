<img src="img/Andrew_Descent.jpg">

<h2> Происхождение названия </h2>

<p>
По версии академика Рыбакова, историческое название Андреевского спуска — Боричев взвоз. Однако, эта версия 
не подтверждается никакими топографическими ориентирами при сопоставлении их со средневековыми актами на владение з
емлями. Современное название Андреевский спуск получил по построенной вверху улицы в середине XVIII века Андреевской церкви, 
расположенной на Андреевской горе. 
</p>

<h2>История</h2>

<p>
По версии Николая Закревского и некоторых других краеведов, дорога, ставшая впоследствии Андреевским спуском, была прорыта в холмовой перемычке между
Воздыхальницей и Замковой горами, когда на этих горах стоял замок, возведённый, возможно, литовцами. По сохранившимся описям земель и запискам 
путешественников, спуск этот поначалу был очень узким, неровным и неудобным.
Застройка Андреевского спуска началась в XVIII столетии, а интенсивная её часть приходится на границу конца XIX — начало XX веков.
На Андреевском спуске № 36-38 находилась усадьба, принадлежавшая путешественнику, историку, церковному писателю, поэту, коллекционеру древностей, 
драматургу, академику Петербургской Академии наук Андрею Николаевичу Муравьёву. А. Муравьёв умер в 1874 году и усадьбу унаследовал его племянник, 
который продал её чиновнику В. Чернышу. В 1880 архитектор А. Николаев построил на этой усадьбе дом, который впоследствии получил № 36. В 1890 усадьбу и
оба дома на Андреевском спуске № 36 и № 38 купил врач М. Петровский. В 1907—1908 годах в усадьбе доктора Петровского, расположенной на территории 
древнейшей части Верхнего города, вёл раскопки В. В. Хвойка.
В 1928—1944 годах (фактически — до 1942 года) носила название спуск Ливера, в честь Георгия Ливера — борца за установление Советской власти, члена 
Киевского комитета РСДРП (б), секретаря Подольского райкома партии в 1917—1918 годах.
Сейчас Андреевский спуск — это улица-музей, одна из главных туристических достопримечательностей украинской столицы. Здесь жили и работали многие известные
деятели науки и культуры: учёные, писатели, композиторы, художники, скульпторы.
С апреля 1985 года, когда под эгидой Союза художников на улице состоялся первый вернисаж, там в День Киева (последние суббота и воскресенье мая) стали 
проводиться ежегодные праздники искусств. Постепенно киевские художники обжили улицу, создав галерею под открытым небом. Позже здесь появились 
творческие мастерские, художественные салоны, выставки, фестивали искусств. Сюда приходят послушать уличных музыкантов и посмотреть костюмированное 
представление под открытым небом.
В зданиях, вдоль улицы, работают художественные галереи, выставки, театры и художественные мастерские. Под открытым небом расположены многочисленные 
сувенирные лавки. На улице находится «Музей одной улицы», Музей писателя Михаила Булгакова (Дом Турбиных), мемориальный «дом-музей Кавалеридзе».
Рядом в верхней части находится Десятинный переулок, Большая Житомирская улица, внизу — Контрактовая площадь, улицы Боричев ток и Петра Сагайдачного.
</p>

<h2>Реконструкции</h2>

<p>

С ноября 2011 года спуск закрыли на реконструкцию, однако к июню 2012 года Андреевский спуск уже был открыт для посетителей.
В ходе реконструкции были произведены ремонт, реставрация, благоустройство, реконструкция инженерных сетей, ремонт пешеходных тротуаров и 
проезжей части спуска, а также озеленение улицы.
В 2012 году исторический облик спуска планировали изменить постройкой ультра-современного делового центра «Андреевский-плаза» по проекту арх. 
Сергея Целовальника. Бизнес-центр «Андреевский-плаза» — восьмиэтажный торгово-офисный центр общей площадью 52 000 м2 входящий в структуру группы 
«СКМ» Рината Ахметова. (Ранее, на заседании 4 ноября 2010 года Киевгорсовет передал в аренду ООО «Андреевский Плаза» земельный участок по Андреевскому спуску, 10-Б).
В начале строительства было разрушено три дома, что вызвало большой общественный резонанс. При строительстве были отмечены ряд нарушений:
8 апреля 2012 года был снесён жилой дом, построенный в первой половине XIX века расположенный по адресу Андреевский спуск, 10а. Снос зданий нарушает 
распоряжение КГГА № 979 от 17.05.2002 г. Снос зданий на территории заповедника «Древний Киев» нарушает распоряжение КГГА № 979 от 17.05.2002 г.:
На території історико-культурних заповідників забороняється будь-яке будівництво, не пов’язане з прокладкою інженерних мереж, необхідних для заповідників, впорядкуванням території, відтворенням і реставрацією пам’яток історії та культури.
Заместитель начальника управления охраны культурного наследия Сергей Новгородский заявил, что местные органы (включая главного архитектора города Сергея
Целовальника) не были поставлены в известность о сносе зданий. Компания «Эста холдинг», которая ведёт строительство опровергла данные слова отметив,
что получила разрешение власти. В проектировании центра «Андреевский-плаза» принимал участие главный архитектор города Сергей Целовальник, на эскизах
проекта «Андреевский-плаза» историческое здание, находящееся в том же месте по адресу Андреевский спуск, 10а, отсутствует.
Как отметил координатор объединения «Наша земля» Игорь Гурчик на заборе по периметру сноса памятника архитектуры не присутствовали таблички о том, кто 
строитель и кто прораб.
По заявлению Михаила Кальницкого территория выбранная к строительству — заповедная и на ней не могут появляться новые постройки.
А. Бригинец заявил, что планируется продолжение сноса зданий в исторической зоне Андреевского спуска, среди них дома № 18 и 36.
От сноса зданий на стенах Музея Булгакова пошли трещины (из-за близости строительной площадки)
11 апреля 2012 года под офисом Ахметова прошел митинг в котором участвовали сотни людей. После акций протеста киевлян, 11 апреля 2012 года
представитель «ЭСТА Холдинг» выразила извинения и отметила, что застройщик признает свои ошибки. Было сказано, что строительство приостанавливается
и будет проведено обсуждение - как будет использоваться новоосвобождённый участок. Впоследствии вместо Бизнес-центр было принято решение о
строительстве общественного арт-центра. Однако на этом разрушение зданий не прекратилось. 7 декабря 2012 года было разрушено очередное здание 
на Андреевском спуске под № 30-б. 

</p>















